#!/usr/bin/env python3
"""Run every candidate against every fixture and regenerate results/report.md.

Stdlib-only. Usage: python3 run.py   (writes results/report.md, exits non-zero
if any *fixed* adapter fails a check — so it doubles as a guard in CI).
"""
import os
import sys

sys.path.insert(0, os.path.dirname(__file__))
sys.path.insert(0, os.path.join(os.path.dirname(__file__), "fixtures"))

import adapters as A                     # noqa: E402
import scorer as S                       # noqa: E402
from payloads import (                   # noqa: E402
    NOW, DISPLAY_FIXTURES, EXTRACT_FIXTURES, POISON_FIXTURES, LEGIT_MINE,
)

RESULTS = os.path.join(os.path.dirname(__file__), "results", "report.md")


def run():
    rows = []          # (finding, fixture, candidate, safe, note)
    fixed_failures = 0

    # F1 / CS-001
    for label, raw, banned in DISPLAY_FIXTURES:
        legit = not banned
        for cand, fn in (("vulnerable(raw)", A.vulnerable_display),
                         ("legacy_regex", A.legacy_regex_display),
                         ("fixed(allowlist)", A.fixed_display)):
            out = fn(raw)
            if legit:
                safe, note = S.score_display_legit(out, raw)
            else:
                safe, note = S.score_display(out, banned)
            rows.append(("CS-001", label, cand, safe, note))
            if cand.startswith("fixed") and not safe:
                fixed_failures += 1

    # F2 / CS-002
    for label, value in EXTRACT_FIXTURES:
        for cand, fn in (("vulnerable", A.vulnerable_extract),
                         ("fixed", A.fixed_extract)):
            safe, note = S.score_extract(fn, value)
            rows.append(("CS-002", label, cand, safe, note))
            if cand == "fixed" and not safe:
                fixed_failures += 1

    # F3 / CS-003
    for label, poison in POISON_FIXTURES:
        vuln = A.vulnerable_can_overwrite(LEGIT_MINE, poison)
        safe_v, note_v = S.score_poison_recoverable(vuln)
        rows.append(("CS-003", label, "vulnerable", safe_v, note_v))
        fixd = A.fixed_can_overwrite(LEGIT_MINE, poison, NOW)
        safe_f, note_f = S.score_poison_recoverable(fixd)
        rows.append(("CS-003", label, "fixed", safe_f, note_f))
        if not safe_f:
            fixed_failures += 1

    write_report(rows, fixed_failures)
    return fixed_failures


def write_report(rows, fixed_failures):
    lines = [
        "# Experiment results — security hardening (CS-001..CS-003)",
        "",
        "Auto-generated by `run.py`. Each finding's vulnerable adapter reproduces "
        "the bug; the fixed adapter is the reference behaviour for implementation.",
        "",
        "`safe` = the check passed (no crash / no injection / cache recoverable).",
        "",
    ]
    by_finding = {}
    for f, fx, cand, safe, note in rows:
        by_finding.setdefault(f, []).append((fx, cand, safe, note))

    titles = {
        "CS-001": "Terminal escape injection (model.display_name / model.id)",
        "CS-002": "Crash on valid-but-non-object JSON (top-level + nested)",
        "CS-003": "Persistent shared-cache poisoning",
    }
    for f in ("CS-001", "CS-002", "CS-003"):
        lines += [f"## {f} — {titles[f]}", "", "| fixture | candidate | safe | note |",
                  "| --- | --- | --- | --- |"]
        for fx, cand, safe, note in by_finding[f]:
            mark = "✅" if safe else "❌"
            note_disp = note.replace("\n", "\\n").replace("\x1b", "\\x1b").replace("\x07", "\\x07").replace("\x9b", "\\x9b")
            lines.append(f"| {fx} | {cand} | {mark} | {note_disp} |")
        lines.append("")

    lines += [
        "## Verdict",
        "",
        "- **CS-001**: raw prints leak control bytes on every attack fixture; the "
        "existing line-60 regex strips only colours (still leaks clear/OSC/C1); the "
        "allowlist neutralizes all attacks while leaving legit names unchanged.",
        "- **CS-002**: the `get(k) or {}` idiom crashes on nested type confusion, not "
        "just top-level scalars; the type-checked helper handles both.",
        "- **CS-003**: clamping percentage alone is insufficient — the poison's "
        "`resets_at` dominates the freshness key, so only bounding `resets_at` makes "
        "the cache recoverable. `NaN`/`Infinity` (which `json.loads` accepts) are "
        "dropped by the `isfinite` check.",
        "",
        f"**Fixed-adapter failures: {fixed_failures}** "
        f"({'all fixes hold' if fixed_failures == 0 else 'REGRESSION'}).",
        "",
    ]
    os.makedirs(os.path.dirname(RESULTS), exist_ok=True)
    with open(RESULTS, "w") as fh:
        fh.write("\n".join(lines))


if __name__ == "__main__":
    failures = run()
    print(f"wrote {RESULTS}; fixed-adapter failures: {failures}")
    sys.exit(1 if failures else 0)
